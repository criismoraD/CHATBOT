CREATE DEFINER=`root`@`localhost` PROCEDURE `buscar_productos` (IN `p_texto` VARCHAR(255), IN `p_categoria` VARCHAR(50), IN `p_genero` VARCHAR(20), IN `p_color` VARCHAR(50), IN `p_talla` VARCHAR(10), IN `p_precio_min` DECIMAL(10,2), IN `p_precio_max` DECIMAL(10,2))   BEGIN
  SELECT DISTINCT
    p.id, p.nombre, p.precio,
    c.nombre AS categoria,
    p.genero, p.stock, p.rating, p.imagen_url
  FROM productos p
  JOIN categorias c              ON p.categoria_id = c.id
  LEFT JOIN producto_colores pc  ON pc.producto_id  = p.id
  LEFT JOIN producto_tallas  pt  ON pt.producto_id  = p.id
  WHERE p.activo = 1
    AND (p_texto      IS NULL OR MATCH(p.nombre,p.descripcion) AGAINST(p_texto IN BOOLEAN MODE)
